package lib;

public class Neighboor {
    private int indice;
    private double valor;

    public Neighboor(int indice, double valor) {
        this.indice = indice;
        this.valor = valor;
    }

    public int getIndice() {
        return indice;
    }

    public void setIndice(int indice) {
        this.indice = indice;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
}

